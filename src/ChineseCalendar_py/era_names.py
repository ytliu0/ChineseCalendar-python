def era_name_lookup(lang, cyear, region, li=None):
    if cyear > 1911: return 'NA'

    eras = [{'y':9999, 'e':'NA'}, {'y':-9999, 'e':'NA'}]
    era = 'NA'
    nian = '年'

    # Spring and autumn period
    if cyear >= -722 and cyear <= -480:
        eras = era_names_spring_autumn(lang, li)

    # Warring states period
    if cyear >= -479 and cyear <= -221:
        eras = era_names_warring_states(lang, li)

    # Qin, Western Han and Xin dynasties
    if cyear >= -220 and cyear <= 25:
        eras = era_names_Qin_Han_Xin(lang)

    # Eastern Han dynasty
    if cyear > 25 and cyear <= 220:
        eras = era_names_Eastern_Han(lang)
        
    # Wei and Jin dynasty
    if cyear > 220 and cyear <= 420 and region=='default':
        eras = era_names_Wei_Jin(lang)

    # Shu dynasty
    if region=='Shu' and cyear >= 221 and cyear <= 263:
        eras = era_names_Shu(lang)

    # Wu dynasty
    if region=='Wu' and cyear >= 222 and cyear <= 280:
        eras = era_names_Wu(lang)

    # Later Qin
    if cyear > 383.5 and cyear < 417.5 and region=='LaterQin':
        eras = era_names_LaterQin(lang)

    # Northern Liang
    if cyear > 410.5 and cyear < 439.5 and region=='NorthernLiang':
        eras = era_names_NorthernLiang(lang)

    # Northern Wei, Western Wei, Northern Zhou and Sui
    if cyear > 385.5 and cyear < 590.5 and region=='WeiZhouSui':
        eras = era_names_WeiZhouSui(lang)

    # Eastern Wei and Northern Qi
    if cyear > 533.5 and cyear < 577.5 and region=='WeiQi':
        eras = era_names_WeiQi(lang)

    # Southern dynasties, Sui dynasty
    if cyear > 420 and cyear < 618 and region=='default':
        eras = era_names_SouthernDynasties_Sui(lang)

    # Tang dynasty
    if cyear >= 618 and cyear <= 705:
        eras = era_names_Tang1(lang)
    if cyear >= 706 and cyear < 907:
        eras = era_names_Tang2(lang)
        if lang=='ChiT':
            if cyear > 743 and cyear < 758: nian = '載'
        elif lang=='ChiS':
            if cyear > 743 and cyear < 758: nian = '载'

    # Five dynasties
    if cyear >= 907 and cyear <= 959 and region=='default':
        eras = era_names_FiveDynasties(lang)

    # Northern Song dynasty
    if cyear >= 960 and cyear <= 1126 and region=='default':
        eras = era_names_NorthernSong(lang)

    # Southern Song dynasty
    if cyear >= 1127 and cyear <= 1279 and region=='default':
        eras = era_names_SouthernSong(lang)

    # Liao, Jin and Mongol/Yuan dynasty in 946-1280
    if cyear > 945.5 and cyear < 1280.5 and region=='LiaoJinYuan':
        eras = era_names_LiaoJinMongol(lang)

    # Yuan dynasty in 1280-1367
    if cyear >= 1280 and cyear <= 1367:
        eras = era_names_Yuan(lang)
            
    # Ming dynasty
    if cyear >= 1368 and cyear <= 1644:
        eras = era_names_Ming(lang)
        
    # Qing dynasty
    if cyear >= 1645 and cyear <= 1911 and region=='default':
        eras = era_names_Qing(lang)

    # Southern Ming and Zheng dynasty
    if cyear >= 1644 and cyear <= 1685 and region=='SouthernMing':
        eras = era_names_SouthernMing(lang)

    n = len(eras)
    for i in range(n):
        ey = cyear - eras[i]['y']
        if ey >= 0:
            era = eras[i]['e']
            if 'offset' in eras[i]: 
                offset = eras[i]['offset']
            else:
                offset = 0
            if offset >= 0 and era != 'NA':
                if lang=='Eng':
                    era += ' ' + str(ey + offset + 1)
                else:
                    cyNum = ['元', '二', '三', '四', '五', '六', '七', '八', '九', '十', '十一', '十二', '十三', '十四', '十五', '十六', '十七', '十八', '十九', '二十', '二十一', '二十二', '二十三', '二十四', '二十五', '二十六', '二十七', '二十八', '二十九', '三十', '三十一', '三十二', '三十三', '三十四', '三十五', '三十六', '三十七', '三十八', '三十九', '四十', '四十一', '四十二', '四十三', '四十四', '四十五', '四十六', '四十七', '四十八', '四十九', '五十', '五十一', '五十二', '五十三', '五十四', '五十五', '五十六', '五十七', '五十八', '五十九', '六十', '六十一', '六十二', '六十三', '六十四', '六十五']
                    era += cyNum[ey + offset] + nian
            break
    return era

